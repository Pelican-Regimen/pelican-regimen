package com.pelicanregimen.ui.components

import androidx.compose.material.MaterialTheme
import androidx.compose.material.Snackbar
import androidx.compose.material.SnackbarData
import androidx.compose.runtime.Composable
import androidx.compose.ui.Modifier
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.graphics.Shape
import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp
import com.pelicanregimen.ui.theme.PelicanRegimenTheme

/**
 * An alternative to [androidx.compose.material.Snackbar] utilizing
 * [com.pelicanregimen.ui.theme.PelicanRegimenColors]
 */
@Composable
fun PelicanRegimenSnackbar(
    snackbarData: SnackbarData,
    modifier: Modifier = Modifier,
    actionOnNewLine: Boolean = false,
    shape: Shape = MaterialTheme.shapes.small,
    backgroundColor: Color = PelicanRegimenTheme.colors.uiBackground,
    contentColor: Color = PelicanRegimenTheme.colors.textSecondary,
    actionColor: Color = PelicanRegimenTheme.colors.brand,
    elevation: Dp = 6.dp
) {
    Snackbar(
        snackbarData = snackbarData,
        modifier = modifier,
        actionOnNewLine = actionOnNewLine,
        shape = shape,
        backgroundColor = backgroundColor,
        contentColor = contentColor,
        actionColor = actionColor,
        elevation = elevation
    )
}
